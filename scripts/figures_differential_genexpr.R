require(tidyverse)
require(limma)
require(magrittr)
require(ggpubr)
require(writexl)
require(latex2exp)

ROOT = here::here()
DATA_DIR = file.path(ROOT,'data')
TCGA_DIR = file.path(DATA_DIR,'raw','UCSCXena','TCGA')
PREP_DIR = file.path(DATA_DIR,'prep')
RESULTS_DIR = file.path(ROOT,'results')

# variables
CANCERS_OI = c('LUSC','UCEC','BRCA','STAD','LUAD','KIRP','THCA','KICH','COAD','LIHC','HNSC','PRAD','KIRC')
SAMPLE_TYPES_OI = c('Solid Tissue Normal','Primary Tumor')
GENE_OI = 'TTLL11'

TEST_METHOD = 'wilcox.test'

FONT_SIZE = 7 # pt
FONT_FAMILY = 'helvetica'

# inputs
genexpr_file = file.path(PREP_DIR,'genexpr_TTLL11.tsv')

# outputs
output_file = file.path(RESULTS_DIR,'figures','differential_expression.pdf')
output_figdata = file.path(RESULTS_DIR,'files','figdata-differential_expression.xlsx')

# load data
df = read_tsv(genexpr_file)

# change sample_type and cancer_type order
df = df %>% 
    mutate(sample_type = factor(sample_type, levels = SAMPLE_TYPES_OI),
           cancer_type = factor(cancer_type, levels = CANCERS_OI))


# plot
make_plot = function(X){
    palette = rev(get_palette('npg',2))
    plt = ggplot(X, aes(cancer_type, expression, fill=sample_type)) + 
        geom_boxplot(outlier.size = 0.1, lwd=0.1) +
        theme_pubr() + 
        stat_compare_means(aes(group = sample_type), 
                           method=TEST_METHOD, label = "p.signif")
    
    plt = set_palette(plt, palette = palette) +
        labs(x=element_blank(), y=element_blank(), fill='Sample Type')
    return(plt)
}

plts = list()
plts[['bycancer']] = make_plot(df) + scale_y_continuous(limits = quantile(df$expression, c(0.001, 0.999))) 
plts[['pancancer']] = make_plot(df %>% mutate(cancer_type=factor('PANCAN'))) + scale_y_continuous(limits = quantile(df$expression, c(0.001, 0.999)))

# compose figure
widths_cancer_type = c(1.15,0.13)

plt = ggarrange(
    plts[['bycancer']] + 
        ylab(TeX('$log_2(Norm. Count + 1)$')) + 
        theme_pubr(base_size = FONT_SIZE) +
        theme(plot.margin = margin(0,0,0,0, "cm")),
    #NULL,
    plts[['pancancer']] + 
        theme_pubr(base_size = FONT_SIZE) +
        theme(plot.margin = margin(0,0,0,0, "cm")),
    widths = widths_cancer_type,
    common.legend = TRUE,
    ncol = 2
) %>% annotate_figure(bottom = text_grob('Cancer Type', 
                                         size = FONT_SIZE, 
                                         family = FONT_FAMILY,
                                         vjust = -0.5)
                     ) +
    theme(plot.margin = margin(0.1,0.1,0.1,0.1, "cm")) 

# prepare figure data
result = df %>% 
    group_by(cancer_type) %>% 
    do(w = wilcox.test(expression ~ sample_type, data=., paired=FALSE)) %>%
    summarize(cancer_type, statistic = w$statistic, 
              p_value = w$p.value, method = w$method, comparison = w$data.name)

sample_summary = df %>%
    group_by(cancer_type, sample_type) %>%
    summarize(n = n(),
              median = median(expression),
              mean = mean(expression),
              std = sd(expression),
              mad = mad(expression))

# save
## figure
ggsave(plt, filename=output_file, units = 'cm', width=12, dpi=300, height=8.5, device=cairo_pdf)

## figure data
write_xlsx(
    x = list(sample_summary = sample_summary, test_result = result),
    path = output_figdata
)

print('Done!')